

import 'package:flutter/material.dart';

class MyTabBarTheme {

  static TabBarTheme get lightTabBarTheme => const TabBarTheme(
    labelColor: Colors.black,
    unselectedLabelColor: Colors.grey,
  );

  static TabBarTheme get darkTabBarTheme => const TabBarTheme(
    labelColor: Colors.black,
    unselectedLabelColor: Colors.grey,
  );

}